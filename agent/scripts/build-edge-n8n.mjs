#!/usr/bin/env node
// Generates supabase/functions/agent/n8n.ts — the Edge-side n8n merge+validate
// module (INTEGRATION_PLAN §3 Wave 4 #15, Edge parity). The Edge is standalone
// Deno and can't import agent/src, so the 5 shipped templates plus the (pure,
// Web-API-only) merger + validator are emitted into one bundled file. The merger
// and validator are ported verbatim from agent/src/n8n (which is unit-tested);
// the template DATA is drift-guarded against agent/n8n_templates/*.json by
// supabase/functions/agent/n8n-drift.test.ts. Re-run: node agent/scripts/build-edge-n8n.mjs

import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const templatesDir = resolve(here, "../n8n_templates");
const outFile = resolve(here, "../../supabase/functions/agent/n8n.ts");

const files = readdirSync(templatesDir)
  .filter((f) => f.endsWith(".json") && f !== "index.json")
  .sort();

const templateEntries = files
  .map((f) => `  ${JSON.stringify(f.replace(/\.json$/, ""))}: ${readFileSync(join(templatesDir, f), "utf8").trim()}`)
  .join(",\n");

const HEADER = `// GENERATED by agent/scripts/build-edge-n8n.mjs — do not edit by hand.
// Edge-side n8n templates + merge + validate (INTEGRATION_PLAN §3 Wave 4 #15).
// Merger/validator ported verbatim from agent/src/n8n (unit-tested); templates
// mirror agent/n8n_templates/*.json (drift-guarded). Pure + Web-API-only (Deno-safe).

export interface N8nNode {
  id: string;
  name: string;
  type: string;
  typeVersion: number;
  position: [number, number];
  parameters: Record<string, unknown>;
  credentials?: Record<string, { id: string; name: string }>;
  webhookId?: string;
}
export type N8nConnections = Record<string, { main: Array<Array<{ node: string; type: "main"; index: number }>> }>;
export interface N8nWorkflow {
  name: string;
  meta: Record<string, unknown>;
  nodes: N8nNode[];
  connections: N8nConnections;
  active: boolean;
  settings: Record<string, unknown>;
  staticData: null | Record<string, unknown>;
  pinData: Record<string, unknown>;
}
export type PlaceholderMap = Record<string, string>;
export interface ValidationResult { valid: boolean; errors: string[] }

function fillStringPlaceholders(value: string, map: PlaceholderMap): string {
  return value.replace(/__([A-Z0-9_]+)__/g, (full, key) => {
    const mapKey = \`__\${key}__\`;
    return mapKey in map ? map[mapKey]! : full;
  });
}
function fillValue(value: unknown, map: PlaceholderMap): unknown {
  if (typeof value === "string") return fillStringPlaceholders(value, map);
  if (Array.isArray(value)) return value.map((v) => fillValue(v, map));
  if (value !== null && typeof value === "object") {
    const result: Record<string, unknown> = {};
    for (const [k, v] of Object.entries(value as Record<string, unknown>)) result[k] = fillValue(v, map);
    return result;
  }
  return value;
}
function reposition(nodes: N8nNode[]): N8nNode[] {
  const X_START = 240, X_STEP = 220, Y = 300;
  return nodes.map((node, i) => ({ ...node, position: [X_START + i * X_STEP, Y] as [number, number] }));
}

export function mergeWorkflow(template: N8nWorkflow, placeholders: PlaceholderMap): N8nWorkflow {
  const nodes: N8nNode[] = template.nodes.map((node) => {
    const merged: N8nNode = {
      ...node,
      id: crypto.randomUUID(),
      parameters: fillValue(node.parameters, placeholders) as Record<string, unknown>,
    };
    if (node.credentials) merged.credentials = fillValue(node.credentials, placeholders) as Record<string, { id: string; name: string }>;
    if (node.webhookId) merged.webhookId = crypto.randomUUID();
    return merged;
  });
  const connections: N8nConnections = {};
  for (const [fromName, targets] of Object.entries(template.connections)) {
    connections[fromName] = { main: targets.main.map((output) => output.map((t) => ({ ...t }))) };
  }
  return { ...template, nodes: reposition(nodes), connections, active: false };
}

const PLACEHOLDER_RE = /__[A-Z0-9_]+__/;
function scanForUnresolved(value: unknown, path: string, found: string[]): void {
  if (typeof value === "string") {
    const match = value.match(/__([A-Z0-9_]+)__/g);
    if (match) for (const m of match) found.push(\`\${path}: unresolved placeholder \${m}\`);
    return;
  }
  if (Array.isArray(value)) { for (let i = 0; i < value.length; i++) scanForUnresolved(value[i], \`\${path}[\${i}]\`, found); return; }
  if (value !== null && typeof value === "object") {
    for (const [k, v] of Object.entries(value as Record<string, unknown>)) scanForUnresolved(v, \`\${path}.\${k}\`, found);
  }
}
export function validateWorkflow(workflow: unknown): ValidationResult {
  const errors: string[] = [];
  if (!workflow || typeof workflow !== "object") return { valid: false, errors: ["workflow is not an object"] };
  const w = workflow as Partial<N8nWorkflow>;
  if (!w.name || typeof w.name !== "string" || w.name.trim() === "") errors.push("workflow.name is missing or empty");
  if (!Array.isArray(w.nodes) || w.nodes.length === 0) {
    errors.push("workflow.nodes must be a non-empty array");
  } else {
    const nodeNames = new Set<string>();
    for (let i = 0; i < w.nodes.length; i++) {
      const node = w.nodes[i];
      if (!node || typeof node !== "object") { errors.push(\`nodes[\${i}]: not an object\`); continue; }
      if (!node.id || typeof node.id !== "string") errors.push(\`nodes[\${i}]: missing id\`);
      else if (PLACEHOLDER_RE.test(node.id)) errors.push(\`nodes[\${i}]: id is still a placeholder: \${node.id}\`);
      if (!node.name || typeof node.name !== "string") errors.push(\`nodes[\${i}]: missing name\`);
      else { if (nodeNames.has(node.name)) errors.push(\`nodes[\${i}]: duplicate node name "\${node.name}"\`); nodeNames.add(node.name); }
      if (!node.type || typeof node.type !== "string") errors.push(\`nodes[\${i}]: missing type\`);
      if (typeof node.typeVersion !== "number" || node.typeVersion < 1) errors.push(\`nodes[\${i}]: typeVersion must be >= 1\`);
      if (!Array.isArray(node.position) || node.position.length !== 2) errors.push(\`nodes[\${i}]: position must be [x, y]\`);
      if (!node.parameters || typeof node.parameters !== "object") errors.push(\`nodes[\${i}]: parameters must be an object\`);
    }
    if (w.connections && typeof w.connections === "object") {
      for (const [connFrom, targets] of Object.entries(w.connections)) {
        if (!nodeNames.has(connFrom)) errors.push(\`connections: source node "\${connFrom}" not in nodes\`);
        if (!targets || !Array.isArray(targets.main)) continue;
        for (const output of targets.main) for (const target of output) {
          if (!nodeNames.has(target.node)) errors.push(\`connections: target node "\${target.node}" not in nodes\`);
        }
      }
    }
  }
  if (!w.connections || typeof w.connections !== "object") errors.push("workflow.connections must be an object");
  if (Array.isArray(w.nodes)) {
    const unresolved: string[] = [];
    for (let i = 0; i < w.nodes.length; i++) scanForUnresolved(w.nodes[i]!.parameters, \`nodes[\${i}].parameters\`, unresolved);
    errors.push(...unresolved);
  }
  return { valid: errors.length === 0, errors };
}
`;

const body = `${HEADER}
export const TEMPLATES: Record<string, N8nWorkflow> = {
${templateEntries}
};
`;

writeFileSync(outFile, body);
console.log(`[build-edge-n8n] wrote ${files.length} templates + merger/validator to supabase/functions/agent/n8n.ts`);
